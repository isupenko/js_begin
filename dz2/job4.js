'use strict'

/**
 * Функция складывает переданные параметры (a + b)
 * @param {number} a 
 * @param {number} b
 * @returns {namber} 
 */
function addition(a, b) {
   return a + b;
}

/**
 * Функция вычитает переданные параметры (a - b)
 * @param {number} a 
 * @param {number} b
 * @returns {namber} 
 */
function deduction (a, b) {
   return a - b;
}

/**
 * Функция делит переданные параметры (a / b)
 * @param {number} a 
 * @param {number} b
 * @returns {namber} 
 */
function division (a, b) {
   return a / b;
}

/**
 * Функция умножает переданные параметры (a * b)
 * @param {number} a 
 * @param {number} b
 * @returns {namber} 
 */
function generation (a, b) {
   return a * b;
}